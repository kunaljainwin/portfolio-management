function NavChart({ data, title, type = "line", theme = "light" }) {
  const chartRef = useRef(null);
  const chartInstance = useRef(null);

  useEffect(() => {
    if (chartInstance.current) {
      chartInstance.current.destroy();
    }

    const ctx = chartRef.current.getContext("2d");

    const isDark = theme === "dark";
    const gridColor = isDark ? "rgba(255, 255, 255, 0.15)" : "rgba(0, 0, 0, 0.1)";
    const borderColor = isDark ? "#54c0f8" : "#1FB8CD";
    const backgroundColor = type === "area" ? (isDark ? "rgba(84, 192, 248, 0.15)" : "rgba(166, 166, 166, 0.1)") : "transparent";
    const textColor = isDark ? "#eee" : "#222";

    const chartData = {
      labels: data.map((item) =>
        new Date(item["NAV Date"]).toLocaleDateString("en-IN", {
          month: "short",
          day: "numeric",
        })
      ),
      datasets: [
        {
          label: "NAV (Rs)",
          data: data.map((item) => item["NAV (Rs)"]),
          borderColor: borderColor,
          backgroundColor: backgroundColor,
          borderWidth: 2,
          fill: type === "area",
          tension: 0.1,
          pointRadius: 1,
          pointHoverRadius: 4,
        },
      ],
    };

    chartInstance.current = new Chart(ctx, {
      type: "line",
      data: chartData,
      options: {
        responsive: true,
        maintainAspectRatio: false,
        color: textColor,
        plugins: {
          legend: {
            display: false,
            labels: { color: textColor },
          },
          tooltip: {
            mode: "index",
            intersect: false,
            enabled: true,
            backgroundColor: isDark ? "#333" : "#fff",
            titleColor: textColor,
            bodyColor: textColor,
            borderColor: borderColor,
            borderWidth: 1,
          },
        },
        scales: {
          x: {
            display: true,
            grid: {
              color: gridColor,
            },
            ticks: {
              color: textColor,
            },
          },
          y: {
            display: true,
            grid: {
              color: gridColor,
            },
            beginAtZero: false,
            ticks: {
              color: textColor,
            },
          },
        },
        interaction: {
          mode: "nearest",
          axis: "x",
          intersect: false,
        },
      },
    });

    return () => {
      if (chartInstance.current) {
        chartInstance.current.destroy();
      }
    };
  }, [data, type, theme]);

  return (
    <div className="chart-container" style={{ color: theme === "dark" ? "#eee" : "#222" }}>
      <h3>{title}</h3>
      <canvas ref={chartRef}></canvas>
    </div>
  );
}

