function App() {
  const [currentPage, setCurrentPage] = useState("home");
  const [theme, setTheme] = useState("light");

  const toggleTheme = () => setTheme(theme === "light" ? "dark" : "light");

  const backgroundColor = theme === "dark" ? "#121212" : "#fff";
  const color = theme === "dark" ? "#ddd" : "#000";

  return (
    <div
      style={{ backgroundColor, color, minHeight: "100vh", display: "flex" }}
    >
      <Sidebar
        currentPage={currentPage}
        setCurrentPage={setCurrentPage}
        theme={theme}
        toggleTheme={toggleTheme}
      />
      <main style={{ marginLeft: "25%", padding: "24px", flexGrow: 1 }}>
        {currentPage === "home" && <HomePage theme={theme}/>}
        {currentPage === "portfolio" && <PortfolioPage theme={theme} />}
      </main>
    </div>
  );
}

